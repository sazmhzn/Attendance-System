package Model;

/**
 *
 * @author lenovo
 */
public class Semester {
    private int id;
    private String name;
    private int c_id;

    public Semester(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public Semester(String name) {
        this.name = name;
    }
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getC_id() {
        return c_id;
    }

    public void setC_id(int c_id) {
        this.c_id = c_id;
    }
    
}
